
    <p>Política de Privacidad para la aplicación que utiliza la API de Facebook

Esta Política de Privacidad describe cómo se recopila, utiliza y comparte información personal cuando utiliza nuestra aplicación que se conecta a la API de Facebook. Al descargar, instalar o utilizar nuestra aplicación, acepta los términos de esta Política de Privacidad.

Recopilación de información

Cuando utiliza nuestra aplicación que se conecta a la API de Facebook, recopilamos información sobre usted a través de la API de Facebook. Esta información puede incluir su nombre de usuario de Facebook, su foto de perfil, sus amigos en Facebook y otra información que haya hecho pública en su perfil de Facebook.

Además, podemos recopilar información sobre su dispositivo móvil, incluyendo el modelo del dispositivo, la versión del sistema operativo, la identificación única del dispositivo y otra información similar.

Uso de la información

Utilizamos la información que recopilamos para mejorar y personalizar su experiencia con nuestra aplicación. También podemos utilizar la información para fines publicitarios y de marketing, incluyendo el envío de mensajes promocionales y publicidad dirigida.

Compartir información

No compartimos información personal con terceros sin su consentimiento, a menos que sea necesario para cumplir con una obligación legal o para proteger nuestros derechos, propiedad o seguridad, o los derechos, propiedad o seguridad de nuestros usuarios u otros.

Seguridad de la información

Tomamos medidas de seguridad razonables para proteger la información personal que recopilamos y almacenamos, incluyendo la utilización de medidas de seguridad físicas, técnicas y administrativas adecuadas para proteger contra el acceso no autorizado, la divulgación, alteración o destrucción de la información personal.

Cambios a la política de privacidad

Podemos actualizar esta Política de Privacidad de vez en cuando para reflejar cambios en nuestras prácticas de privacidad. Si realizamos cambios significativos en la forma en que recopilamos, utilizamos o compartimos su información personal, le notificaremos a través de la aplicación o por otros medios.

Contacto

Si tiene preguntas o inquietudes sobre esta Política de Privacidad, por favor póngase en contacto con nosotros a través de la aplicación o por correo electrónico a privacy@ourapp.com.</p>
